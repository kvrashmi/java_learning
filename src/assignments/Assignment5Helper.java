package assignments;

public class Assignment5Helper {
	
	public static int add(int n1,int n2)
	{
		return n1+n2;
	}
	
	public static int sub(int n1,int n2)
	{
		return n1-n2;
	}
	
	public int mul(int n1,int n2)
	{
		return n1*n2;
	}
	
	public int div(int n1,int n2)
	{
		return n1/n2;
	}
	
	public boolean checkEvenOrOdd(int n1)
	{
		if(n1%2==0) 
		{
			return true;
		}
		return false;
	}
	
}
